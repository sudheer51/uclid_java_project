
public class Loops {

	public static void main(String[] args) {
	 
		
		for(int i=1;i<=100;i++)
		{
			System.out.print(i);
		}
		
		
		int j=10;
		while(j<20)
		{
			j++;
			System.out.println(j);
			//allow join
			//sleep and check after 2 minutes
			//readnoofStudents
		}
		
		int k=10;
		do
		{
			System.out.println("in do-while" + k );
			k++;
			
		}while(k>20);
	}
}
